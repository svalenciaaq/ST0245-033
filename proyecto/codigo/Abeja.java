
public class Abeja {
	int x;
	int y;
	int val;
	int radio;
	
	public Abeja(int x,int y,int val) {
		this.x=x;
		this.y=y;
		this.radio=100;
		this.val=val;
		
	}
	
	


	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}


	public double getRadio() {
		return radio;
	}


	public void setRadio(int radio) {
		this.radio = radio;
	}
	
	

}
